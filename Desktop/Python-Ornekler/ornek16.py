# Örnek 16:Kenarları Girilen Dikdörtgenin Alanı ve Çevresini Bulan Python Örneği

kisakenar=int(input("Kısa kenarı giriniz."))
uzunkenar=int(input("Uzun kenarı giriniz."))
alan=kisakenar*uzunkenar
cevre=2*(kisakenar+uzunkenar)
print("Dikdörtgenin Alanı : "+str(alan)+"\nDikdörtgenin Çevresi : "+str(cevre))